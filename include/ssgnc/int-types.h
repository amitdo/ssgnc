#ifndef SSGNC_INT_TYPES_H
#define SSGNC_INT_TYPES_H

#include <cstddef>

namespace ssgnc {

typedef char Int8;
typedef short Int16;
typedef int Int32;
typedef long long Int64;

typedef unsigned char UInt8;
typedef unsigned short UInt16;
typedef unsigned int UInt32;
typedef unsigned long long UInt64;

}  // namespace ssgnc

#endif  // SSGNC_INT_TYPES_H
